#ifndef OLN_TYPES_H
#define OLN_TYPES_H

#include <stdint.h>

// 8-bit
typedef int8_t i8;
typedef uint8_t u8;

// 16-bit
typedef int16_t i16;
typedef uint16_t u16;

// 32-bit
typedef int32_t i32;
typedef uint32_t u32;

// 64-bit
typedef int64_t i64;
typedef uint16_t u64;

// floats
typedef float f32;
typedef double f64;

#endif // !OLN_TYPES_H
